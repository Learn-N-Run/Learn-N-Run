package dto;

public class Bank_NameDTO {
	private int no;
	private String name;

	public Bank_NameDTO() {
	}

	public Bank_NameDTO(int no, String name) {
		super();
		this.no = no;
		this.name = name;
	}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
